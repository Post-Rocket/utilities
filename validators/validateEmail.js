'use strict';

// Helper function to validate an email string.
// Example:
// validateEmail('1234567890') will return false (i.e. not validated)
export const RE = /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/i;
export const validateEmail = email => email && typeof email === 'string' && RE.test(email.replace(/s+/g, ''));

// Exports.
Object.defineProperty(validateEmail, 'RE', {
  value: RE
});
export default Object.freeze(Object.defineProperty(validateEmail, 'validateEmail', {
  value: validateEmail
}));

// Node.js exports.
try {
  module.exports = validateEmail;
} catch {}